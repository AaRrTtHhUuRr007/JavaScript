//Manipulando Array

const arr = [4,3,6,5]
console.log(arr)

arr.unshift(8)//adiciona um item no inicio
console.log(arr)

arr.shift()//retira um item no inicio
console.log(arr)

arr.push(6)//adiciona im item no final
console.log(arr)

arr.pop()//retira um item no final 
console.log(arr)

arr.sort()// ordena o array 
console.log(arr)




